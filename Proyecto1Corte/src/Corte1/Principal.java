        File arch=new File("./data/empleados.properties");
		Properties props=new Properties();
		FileInputStream fis=new FileInputStream(arch);
		props.load(fis);
		String cantEmpleados = props.getProperty("cantidadEmpleados");
                int intCantEmpleados = Integer.parseInt(cantEmpleados);
                
                Empleado empleado = null;
