    private void btnAgregarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarSolicitudActionPerformed
        if(lstUsuarios.isSelectionEmpty()){
           JOptionPane.showMessageDialog(this,"seleccione un usuario");
       }else{
            String strDescripcion = txtDescripcion.getText();
            if (strDescripcion.equals("")) {
                JOptionPane.showMessageDialog(this,"Descripcion es requerida");
            } else {
                solicitud = new Solicitud();
                Usuario usuario = (Usuario) lstUsuarios.getSelectedValue();
                solicitud.setId(cantSolicitudes++);
                solicitud.setDescripcion(strDescripcion);
                solicitud.setUsuario(usuario);
                TipoSolicitud tipo = (TipoSolicitud) cmbTipos.getSelectedItem();
                solicitud.setTipo(tipo);
                solicitudes.add(solicitud);
                 for (Empleado empleado : empleados) {
                     if(empleado.getTipoSolicitud().toString().equals(solicitud.getTipo().toString())){
                         solicitud.setIdEmpleado(empleado.getId());

                     }
                 }
                 for (Area area : areas) {
//                     System.out.println(area.getNombre());
//                     System.out.println(solicitud.getTipo().getNombre());
                     if(area.getNombre().toString().equals(solicitud.getTipo().getNombre())){
                         solicitud.setIdArea(area.getId());
                     }
                 }
                lstSlicitudes.setListData(solicitudes.toArray());
            }
           
       }
    }//GEN-LAST:event_btnAgregarSolicitudActionPerformed

